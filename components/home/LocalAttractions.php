                <h1 class="danger">Hmawbi Firelight Camps</h1>
                <p>
                    Experience nature from the comfort of a luxury platform tent at Firelight Camps, located in the woods just outside Hmawbi, Myanmar. A stay includes breakfast, access to a bathhouse with hot showers and flushing toilets, and communal areas with fire pits and games. It’s definitely glamping, but you can head into Buttermilk Falls State Park to hike in the great outdoors or explore the Finger Lakes region, where there’s biking and boating. This campground is ideal for those who want quick access to amenities like restaurants, bars and wineries, but still want to find some quiet amongst the trees. Some of the tents come with electric heaters, which you should consider when booking in the spring or fall.
