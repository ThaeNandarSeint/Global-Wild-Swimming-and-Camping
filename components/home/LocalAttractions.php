                <h1 class="primary">Inya Lake Campground</h1>
                <p>
                    Inya Lake has a plethora of camping available on its islands, but one of the most scenic is in National Park at Campground on Bahan. The area is lush, filled with streams and waterfalls, and the views of the coast are unparalleled. The campground is slightly remote, although you can drive in, and it’s limited to three days. There are toilets and picnic tables, as well as a water-refilling station, but be prepared to be away from civilization during your stay and pack both sun and rain gear. Once situated, it’s the hiking trails that will draw you in, starting with the four-mile Kandawgyi Trail. It’s a side of Inya Lake you definitely won’t find at an all-inclusive resort. Although the campground is out of the way, it is accessible to visitors with mobility concerns.
                </p>
